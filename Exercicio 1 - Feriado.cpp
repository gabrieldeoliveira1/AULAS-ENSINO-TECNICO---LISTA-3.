#include <stdio.h>
#include <stdlib.h>

int main() {
    int num;

    printf("Digite um numero inteiro: ");
    scanf("%d", &num);

    if (num > 0) {
        printf("\nO numero digitado e positivo!");
    } else if (num < 0) {
        printf("\nO numero digitado e negativo! ");
    } else {
        printf("\nO numero digitado e zero!");
    }

    return 0;
}

		
		
	
	
	

		
		


	